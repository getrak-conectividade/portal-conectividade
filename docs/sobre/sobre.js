let anoAtual = null;
let tipoAtual = 'despesa';
let operadorAtual = null;

const rotulos = { despesa: 'Despesa', receita: 'Receita', desconto: 'Desconto', estoque: 'Custo por Linha' };

function renderModalMensal() {
    const dados = tipoAtual === 'estoque'
        ? dadosMensais.estoque[operadorAtual]?.[anoAtual]
        : dadosMensais[tipoAtual][anoAtual];
    if (!dados) return;

    modalTitulo.textContent = tipoAtual === 'estoque'
        ? `Custo por Linha — ${rotulosOperadora[operadorAtual]} (${anoAtual})`
        : `${rotulos[tipoAtual]}s Mensais — ${anoAtual}`;
    modalColunaValor.textContent = rotulos[tipoAtual];
    modalTabelaCorpo.innerHTML = dados.meses
        .map(([mes, valor]) => `<tr><td>${mes}</td><td>${valor}</td></tr>`)
        .join('');
    modalTotal.textContent = dados.total;
    modalTotalLabel.textContent = tipoAtual === 'estoque' ? 'Média' : 'Total';
    modalMensal.classList.toggle('tipo-receita', tipoAtual === 'receita');
    modalMensal.classList.toggle('tipo-desconto', tipoAtual === 'desconto');
}

function abrirModalMensal(ano, tipoInicial = 'despesa', operador = null) {
    const existe = tipoInicial === 'estoque'
        ? dadosMensais.estoque[operador]?.[ano]
        : dadosMensais[tipoInicial][ano];
    if (!existe) return;

    anoAtual = ano;
    tipoAtual = tipoInicial;
    operadorAtual = operador;

    const temTabsTipo = tipoInicial !== 'desconto' && tipoInicial !== 'estoque';
    modalTabsTipoWrap.style.display = temTabsTipo ? '' : 'none';
    if (temTabsTipo) {
        modalTabsTipo.forEach(tab => tab.classList.toggle('active', tab.dataset.tipo === tipoAtual));
    }

    const temTabsAno = tipoInicial === 'estoque';
    modalTabsAnoWrap.style.display = temTabsAno ? '' : 'none';
    if (temTabsAno) {
        modalTabsAno.forEach(tab => tab.classList.toggle('active', tab.dataset.ano === anoAtual));
    }

    renderModalMensal();

    modalMensal.classList.add('open');
    document.body.style.overflow = 'hidden';
}

function fecharModalMensal() {
    modalMensal.classList.remove('open');
    document.body.style.overflow = '';
}

